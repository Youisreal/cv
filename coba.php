<!DOCTYPE html>
<html>

<head>
    <title>Open to More Info</title>
    <style>
        .hidden {
            display: none;
        }
    </style>
</head>

<body>

    <h1>Judul Halaman</h1>

    <button onclick="toggleInfo()">Open to More Info</button>

    <div id="moreInfo" class="hidden">
        <p>Ini adalah informasi tambahan yang bisa dibuka atau ditutup.</p>
        <p>Ini juga bisa berisi konten lain yang ingin Anda tambahkan.</p>
    </div>

    <script>
        function toggleInfo() {
            var moreInfo = document.getElementById('moreInfo');
            if (moreInfo.style.display === 'none') {
                moreInfo.style.display = 'block';
                document.querySelector('button').innerText = 'Close Info';
            } else {
                moreInfo.style.display = 'none';
                document.querySelector('button').innerText = 'Open to More Info';
            }
        }
    </script>

</body>

</html>